                    <h2>CONHEÇA O GAAR</h2>

                    <p>
                    	O GAAR é uma ONG que luta pelo bem-estar animal há mais de uma década. Atua em Campinas, promovendo a conscientização para a guarda responsável de cães e gatos, realizando um trabalho contínuo de castração, vacinação e doação desses animais. Nossa esperança é que, com esta ação, num futuro próximo bicho abandonado seja coisa do passado.<br />
                    <br />
                    	Não temos abrigo e, por isso, não fazemos recolhimento de animais. Mas, podemos orientar e intermediar procedimentos veterinários a preços acessíveis para todos os que se sintam motivados a dar uma mãozinha a um animal abandonado.
