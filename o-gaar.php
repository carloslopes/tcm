                    <h3 class="abre">Princípios Básicos para o Bem-Estar Animal</h3>

                    <p class="abre-conteudo">
                    	- Nunca deixe seu animal solto nas ruas para que ele não seja vítima de acidentes ou maus-tratos.<br />
                        <br />
                        - Alimente-o de forma regular, e com os nutrientes necessários.<br />
                        <br />
                        - Não se esqueça de lhe dar água, sempre limpa e fresca.<br />
                        <br />
                        - Mantenha a higiene do local, para evitar problemas de saúde.<br />
                        <br />
                        - Combata pulgas e carrapatos.<br />
                        <br />
                        - Providencie espaço adequado e o proteja do sol, da chuva e do frio.<br />
                        <br />
                        - Identifique seu animal com plaqueta contendo seu telefone para o caso de fuga.<br />
                        <br />
                        - A vacinação é importantíssima, mas atenção - só no veterinário - procure um dos veterinários parceiros do Gaar e consiga vacina de boa qualidade a um preço especial.<br />
                        <br />
                        - Acima de tudo, trate seu animal com carinho e respeito.<br />
                        <br />
                        - Jamais o abandone.<br />
                        <br />
                        - Ele não sabe viver sem você.
                    </p>

