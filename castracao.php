                    <h3 class="abre">ESTERILIZAÇÃO (CASTRAÇÃO): O QUE É?</h3>

                    <p class="abre-conteudo">
                    	Esterilizar é prevenir o abandono.<br />
                        <br />
                        Além de prevenir doenças, a esterilização garante o bem-estar dos animais e representa a única medida eficaz no controle das populações de cães e gatos.<br />
                        <br />
                        Somente com a esterilização diminuirá o número de animais abandonados e sacrificados, pois a principal causa do abandono é o descontrole populacional. Uma cadela não esterilizada e seus descendentes podem gerar, em seis anos, 64.000 animais. E não existem lares responsáveis para todos.<br />
                        <br />
                        Impedir a cada cio que o animal cruze, mas mantê-lo com os instintos para isso, representa uma constante frustração. A esterilização de cães e gatos, fêmeas e machos, é uma cirurgia que impedirá a procriação. Ela deverá ser feita por médico veterinário experiente, e o animal deverá estar sob o efeito de anestesia geral. A esterilização do macho é mais fácil e mais rápida. A esterilização das fêmeas - que consiste na retirada de útero e ovários - requer alguns dias de atenção após a cirurgia, até a completa cicatrização. Atualmente muitos veterinários utilizam nova técnica para a esterilização das fêmeas, menos invasiva, na qual o corte é menor, tornando a cirurgia e a recuperação mais rápidas. Consulte seu veterinário ou faculdades de veterinária e entidades de proteção animal.<br />
   						<br />
                        <strong>Para os machos:</strong> Um macho esterilizado deixa de fugir tentando ir atrás de fêmeas no cio, tem menos necessidade de marcar território com urina, porém continua guardião da casa e da família.<br />
                        <br />
                        <strong>Para as fêmeas:</strong> Uma fêmea esterilizada deixa de atrair a legião de machos à sua porta, não tenta fugir para cruzar e não tem mais cio, ou seja, não menstrua mais. Além disso, ela estará se livrando da piometra (infecção no útero) que atinge em média 60% das cadelas não esterilizadas, cujo tratamento inclui a esterilização.<br />
                        <br />
                        Esterilize seu animal (macho ou fêmea) antes da puberdade. Não é necessário aguardar o primeiro cio de sua cadela ou gata para esterilizar. E recomendada a esterilização antes da puberdade, a partir de 2 meses de idade, cujas vantagens são:<br />
                        - saúde: cadelas e gatas esterilizadas antes da puberdade reduzem em mais de 90% as chances de terem câncer de mama;<br />
                        - conforto e bem-estar: a recuperação pós-cirurgia é mais rápida.<br />
