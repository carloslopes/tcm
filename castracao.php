                    	Muito se discute sobre a castração de animais domésticos. Contudo, é comum encontrar pessoas que pouco ou nada sabem sobre o assunto. Castração é um ato de amor e respeito para com os animais. Para entender isso, basta considerar os seguintes fatos:<br />
                    <br />
                    1. Os animais se reproduzem em progressão geométrica, ou seja, uma cadela não castrada e seus descendentes gerarão cerca de 60.000 (sessenta mil!) filhotes em apenas seis anos.<br />
