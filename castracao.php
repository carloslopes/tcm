                    2. Quando falamos de gatos o número salta para 420.000 (quatrocentos e vinte mil!) novos animais! Cadelas se reproduzem a cada 06 meses, as gatas a cada 03 meses!   São números astronômicos considerando que fica praticamente impossível encontrar donos responsáveis para todos estes animais. Não castrar gera então o abandono, animais em condições péssimas e a proliferação de doenças.
