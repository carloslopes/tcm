                    <h3 class="abre">O QUE NÃO FAZER</h3>

                    <p class="abre-conteudo">
                        Não aplique injeção anticoncepcional (hormônio) em fêmeas, ela é totalmente contra-indicada. Se o fizer, você estará prejudicando a cadela, talvez condenando-a a uma morte sofrida e precoce. O único método de controle populacional indicado pela Organização Mundial da Saúde é a esterilização cirúrgica de fêmeas e machos caninos ou felinos. Hoje existe também a esterilização química de machos felinos e caninos.<br />
                        <br />
                        As injeções de hormônio prejudicam as fêmeas e podem provocar a piometra (infecção do útero), cujo tratamento inclui a urgente esterilização (do contrário o animal pode vir a óbito por septicemia).<br />
                        <br />
                        Há ainda o perigo de os guardiões daquele animal não perceberem a tempo os sintomas da piometra, que podem ser inapetência (nem todos os animais) e corrimento (discreto) de secreção amarelada (pus) pela vagina. Se for um animal de rua ou semi-domiciliado, dificilmente serão percebidos os sintomas.<br />
                        <br />
                        Veja abaixo a opinião da Dra. Vanessa Mollica Caetano Teixeira - Médica veterinária - Especialista em clínica e cirurgia – UFV Mestre em cirurgia – Unesp:<br />
                        <br />
                        "O uso de anticoncepcionais é um dos principais causadores de aparecimento de tumores de mama, infecções uterinas e tumores uterinos e de ovário, além de predisporem a doenças endócrinas, como o hiperadrenocorticismo, e promoverem resistência insulínica, provocando o surgimento da diabetes mellitus. Também pode ser observada falha, ausência ou descoloração do pelo no local da aplicação.<br />
                        <br />
                        No caso da infecção de útero, o tratamento é a retirada do órgão de forma emergencial, antes que o animal entre no quadro de infecção generalizada, toxemia associada à insuficiência renal, colapso e morte."
                    </p>

