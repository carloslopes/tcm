                    	Submeter um animal a maus-tratos é crime previsto no artigo 32 da Lei de Crimes Ambientais (Lei Federal nº 9.605) e pode acarretar em multa ou pena de três meses a um ano de prisão. Denunciar é simples e quem procura ajuda não fica exposto à represálias do agressor.<br />
                    	<br />
                        De acordo com o artigo 3 do Decreto Federal 24.645/34 caracteriza-se por maus-tratos manter o animal trancado em lugares pequenos, anti-higiênicos ou preso a correntes, golpear ou mutilar o animal, não prestar assistência veterinária adequada ou usá-lo em shows que causem pânico ou estresse. Envenenar e abandonar animais também são atos criminosos.<br />
