                    <h3>DENUNCIE MAUS TRATOS!</h3>

                    <p>
                        <strong>O que fazer</strong><br />
                        <br />
                        O primeiro passo é reunir a maior quantidade de provas possível. Fotografias, vídeos, laudo ou atestado veterinário, placa do carro de quem agride ou abandona e até testemunhas. Tudo o que sirva para mostrar a situação do animal e ajude a identificar o agressor.<br />
                        <br />
                        Em seguida procure a delegacia mais próxima e faça um Boletim de Ocorrência (BO). Por garantia, leve com você uma cópia da Lei de Crimes Ambientais (disponível no site do Planalto:<br />
                        <br />
                        Clique <a href="http://www.planalto.gov.br/CCIVIL_03/leis/L9605.htm" target="_blank">aqui</a> para acessar ou tenha escrito o número da lei e o que diz o artigo 32**.<br />
                        <br />
                        O policial que ouvir o seu relato deverá instaurar um inquérito ou lavrar um Termo Circunstanciado (TC). Caso ele se negue a fazê-lo estará infringindo o Código Penal (artigo 319, crime de prevaricação). Na dúvida, e porque é seu direito, peça para falar com o delegado.<br />
                        <br />
                        No Brasil os animais são “sujeitos de direitos”, tutelados pelo Estado e representados em juízo pelo Ministério Público. Portanto, você não será autor do processo judicial que for aberto a partir da sua denúncia.<br />
          				<br />
                        Depois de concluído o inquérito para apuração do crime, ou elaborado o Termo Circunstanciado, o delegado o encaminhará para abertura da ação, na qual constará o Estado como autor.<br />
                    	<br />
                        <strong>Boletim de Ocorrência Eletrônico</strong><br />
                        <br />
                        Em São Paulo e na Grande São Paulo os BOs podem ser feitos também pela internet, no site <a href="http://www.seguranca.sp.gov.br" target="_blank">http://www.seguranca.sp.gov.br</a>. Depois de preencher o formulário, a polícia entrará em contato para confirmar as informações e, se estiver tudo certo, você receberá uma cópia do documento por email.<br />
                        <br />
                        Se ainda assim você estiver com receio de procurar uma delegacia e denunciar, busque orientações no fórum mais próximo da sua casa ou peça ajuda da associação de bairro da sua região. Representantes da população, as associações de bairro têm poder de exigir atitudes das autoridades em favor da comunidade e você pode pedir que um dirigente ou alguém designado te acompanhe até a delegacia ou ao fórum.<br />
